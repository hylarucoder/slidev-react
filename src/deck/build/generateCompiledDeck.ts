import { createHash } from 'node:crypto'
import { mkdir, readdir, readFile, unlink, writeFile } from 'node:fs/promises'
import path from 'node:path'
import { compile } from '@mdx-js/mdx'
import type { RsbuildPlugin } from '@rsbuild/core'
import { parseDeck } from '../parsing/parseDeck'
import { getMdxCompileOptions } from '../compiling/mdx-options'

const GENERATED_DECK_DIR = '.generated/deck'
const GENERATED_SLIDES_DIR = `${GENERATED_DECK_DIR}/slides`
const GENERATED_MANIFEST_FILE = `${GENERATED_DECK_DIR}/index.ts`

function hashString(source: string) {
  return createHash('sha256').update(source).digest('hex').slice(0, 12)
}

function toModuleIdentifier(slideId: string) {
  return slideId.replace(/[^a-zA-Z0-9_$]/g, '_')
}

function createSlideModuleFileName(index: number) {
  return `slide-${index + 1}.tsx`
}

function createSlideModuleCode(compiledSource: string) {
  return [
    '/* eslint-disable */',
    '/* @generated by compile-time deck pipeline */',
    compiledSource.trim(),
    '',
  ].join('\n')
}

function normalizeDiagramCodeProps(compiledSource: string) {
  return compiledSource.replace(
    /<(MermaidDiagram|PlantUmlDiagram) code="([\s\S]*?)" \/>/g,
    (_match, componentName: string, code: string) =>
      `<${componentName} code={${JSON.stringify(code)}} />`,
  )
}

function createManifestCode({
  deckSourceHash,
  deckMeta,
  slideEntries,
}: {
  deckSourceHash: string
  deckMeta: Record<string, unknown>
  slideEntries: Array<{
    id: string
    importName: string
    importPath: string
    meta: Record<string, unknown>
  }>
}) {
  const imports = slideEntries
    .map(entry => `import ${entry.importName} from '${entry.importPath}'`)
    .join('\n')
  const slides = slideEntries
    .map(entry => `    {
      id: ${JSON.stringify(entry.id)},
      meta: ${JSON.stringify(entry.meta, null, 6)},
      component: ${entry.importName},
    }`)
    .join(',\n')

  return [
    '/* eslint-disable */',
    '/* @generated by compile-time deck pipeline */',
    "import type { CompiledDeckManifest } from '../../src/deck/model/compiled-deck'",
    imports,
    '',
    'const compiledDeck: CompiledDeckManifest = {',
    `  sourceHash: ${JSON.stringify(deckSourceHash)},`,
    `  meta: ${JSON.stringify(deckMeta, null, 2)},`,
    '  slides: [',
    slides,
    '  ],',
    '}',
    '',
    'export default compiledDeck',
    '',
  ].join('\n')
}

async function writeIfChanged(filePath: string, content: string) {
  try {
    const current = await readFile(filePath, 'utf8')
    if (current === content)
      return
  }
  catch {
    // File does not exist yet.
  }

  await mkdir(path.dirname(filePath), { recursive: true })
  await writeFile(filePath, content, 'utf8')
}

async function removeStaleGeneratedSlides(slidesDir: string, expectedFiles: Set<string>) {
  try {
    const files = await readdir(slidesDir)
    await Promise.all(
      files.map(async (fileName) => {
        if (!expectedFiles.has(fileName))
          await unlink(path.join(slidesDir, fileName))
      }),
    )
  }
  catch {
    // Directory does not exist yet.
  }
}

async function compileSlideModule(source: string) {
  const compiled = await compile(source, {
    ...getMdxCompileOptions(),
    development: false,
    jsx: true,
    jsxImportSource: 'react',
    providerImportSource: '@mdx-js/react',
  })

  return normalizeDiagramCodeProps(String(compiled))
}

export async function generateCompiledDeckArtifacts(options: {
  appRoot: string
  deckSourceFile: string
}) {
  const { appRoot, deckSourceFile } = options
  const deckSource = await readFile(deckSourceFile, 'utf8')
  const parsedDeck = parseDeck(deckSource)
  const sourceHash = hashString(deckSource)
  const generatedDeckDir = path.join(appRoot, GENERATED_DECK_DIR)
  const generatedSlidesDir = path.join(appRoot, GENERATED_SLIDES_DIR)
  const manifestFile = path.join(appRoot, GENERATED_MANIFEST_FILE)
  const expectedSlideFiles = new Set<string>()
  const manifestEntries: Array<{
    id: string
    importName: string
    importPath: string
    meta: Record<string, unknown>
  }> = []

  await mkdir(generatedSlidesDir, { recursive: true })

  for (const slide of parsedDeck.slides) {
    const slideFileName = createSlideModuleFileName(slide.index)
    const slideFilePath = path.join(generatedSlidesDir, slideFileName)
    const importName = toModuleIdentifier(slide.id)

    try {
      const compiledSource = await compileSlideModule(slide.source)
      await writeIfChanged(slideFilePath, createSlideModuleCode(compiledSource))
    }
    catch (error) {
      const slideName = slide.meta.title
        ? `${slide.index + 1} (${slide.meta.title})`
        : String(slide.index + 1)
      throw new Error(`Failed to compile slide ${slideName}: ${error instanceof Error ? error.message : String(error)}`, {
        cause: error,
      })
    }

    expectedSlideFiles.add(slideFileName)
    manifestEntries.push({
      id: slide.id,
      importName,
      importPath: `./slides/${path.basename(slideFileName, path.extname(slideFileName))}`,
      meta: slide.meta,
    })
  }

  await removeStaleGeneratedSlides(generatedSlidesDir, expectedSlideFiles)
  await writeIfChanged(manifestFile, createManifestCode({
    deckSourceHash: sourceHash,
    deckMeta: parsedDeck.meta,
    slideEntries: manifestEntries,
  }))

  return {
    generatedDeckDir,
    manifestFile,
    sourceHash,
  }
}

class WatchDeckSourcePlugin {
  private readonly deckSourceFile: string

  constructor(deckSourceFile: string) {
    this.deckSourceFile = deckSourceFile
  }

  apply(compiler: {
    hooks: {
      thisCompilation: {
        tap: (name: string, callback: (compilation: { fileDependencies?: { add: (file: string) => void } }) => void) => void
      }
    }
  }) {
    compiler.hooks.thisCompilation.tap('watch-deck-source', (compilation) => {
      compilation.fileDependencies?.add(this.deckSourceFile)
    })
  }
}

export function pluginCompileTimeDeck(options: {
  appRoot: string
  deckSourceFile: string
}): RsbuildPlugin {
  const { appRoot, deckSourceFile } = options

  return {
    name: 'slide-react-compile-time-deck',
    setup(api) {
      const generate = async () => {
        await generateCompiledDeckArtifacts({
          appRoot,
          deckSourceFile,
        })
      }

      api.onBeforeBuild(generate)
      api.onBeforeDevCompile(generate)
      api.modifyRspackConfig((config) => {
        config.plugins ||= []
        config.plugins.push(new WatchDeckSourcePlugin(deckSourceFile) as never)
      })
    },
  }
}

